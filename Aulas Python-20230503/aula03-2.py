# operadores artiméticos

#entrada
a=float(input('Entre com o primeiro numero: '))
b=float(input('Entre com o segundo numero: '))

#processamento
## adição
adicao = a + b

## subtração
subtracao = a - b

## multiplicação
multiplicacao = a * b

## divisão
divisao = a / b

## modulo
modulo = a % b

## divisão inteira
divisao_inteira = a // b

#saida
print('Resultado da adição: ', adicao)
print('Resultado da subtração: ',subtracao)
print('Resultado na multiplicação: ',multiplicacao)
print('Resultado da divisão: ',divisao)
print('Resultado do módulo: ',modulo)
print('Resultado da divisão inteira: ',divisao_inteira)